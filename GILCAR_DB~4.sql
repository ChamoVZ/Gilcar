---- PKG_VEHICULOS (CRUD + cursores) 

CREATE OR REPLACE PACKAGE PKG_VEHICULOS AS
  PROCEDURE SP_LISTAR(p_out OUT SYS_REFCURSOR);
  PROCEDURE SP_LISTAR_DISPONIBLES(p_out OUT SYS_REFCURSOR);
  PROCEDURE SP_OBTENER_POR_ID(p_id IN NUMBER, p_out OUT SYS_REFCURSOR);

  PROCEDURE SP_INSERTAR(
    p_marca        IN VARCHAR2,
    p_modelo       IN VARCHAR2,
    p_ano          IN NUMBER,
    p_tipo         IN VARCHAR2,
    p_km           IN NUMBER,
    p_combustible  IN VARCHAR2,
    p_precio       IN NUMBER,
    p_color        IN VARCHAR2,
    p_descripcion  IN VARCHAR2,
    p_id_out       OUT NUMBER
  );

  PROCEDURE SP_ACTUALIZAR(
    p_id           IN NUMBER,
    p_marca        IN VARCHAR2,
    p_modelo       IN VARCHAR2,
    p_ano          IN NUMBER,
    p_tipo         IN VARCHAR2,
    p_km           IN NUMBER,
    p_combustible  IN VARCHAR2,
    p_precio       IN NUMBER,
    p_color        IN VARCHAR2,
    p_descripcion  IN VARCHAR2
  );

  PROCEDURE SP_INACTIVAR(p_id IN NUMBER);
END PKG_VEHICULOS;
/


CREATE OR REPLACE PACKAGE BODY PKG_VEHICULOS AS

  PROCEDURE SP_LISTAR(p_out OUT SYS_REFCURSOR) IS
  BEGIN
    OPEN p_out FOR
      SELECT ID_VEHICULO,
             MARCA,
             MODELO,
             ANO_FABRICACION,
             TIPO_VEHICULO,
             KILOMETRAJE,
             TIPO_COMBUSTIBLE,
             PRECIO,
             NVL(COLOR,'') AS COLOR,
             ESTADO_VEHICULO,
             NVL(DESCRIPCION,'') AS DESCRIPCION
        FROM VEHICULOS
       ORDER BY ID_VEHICULO;
  END;

  PROCEDURE SP_LISTAR_DISPONIBLES(p_out OUT SYS_REFCURSOR) IS
  BEGIN
    OPEN p_out FOR
      SELECT ID_VEHICULO,
             MARCA,
             MODELO,
             ANO_FABRICACION,
             TIPO_VEHICULO,
             KILOMETRAJE,
             TIPO_COMBUSTIBLE,
             PRECIO,
             NVL(COLOR,'') AS COLOR,
             ESTADO_VEHICULO,
             NVL(DESCRIPCION,'') AS DESCRIPCION
        FROM VEHICULOS
       WHERE ESTADO_VEHICULO = 'DISPONIBLE'
       ORDER BY ID_VEHICULO;
  END;

  PROCEDURE SP_OBTENER_POR_ID(p_id IN NUMBER, p_out OUT SYS_REFCURSOR) IS
  BEGIN
    OPEN p_out FOR
      SELECT ID_VEHICULO,
             MARCA,
             MODELO,
             ANO_FABRICACION,
             TIPO_VEHICULO,
             KILOMETRAJE,
             TIPO_COMBUSTIBLE,
             PRECIO,
             NVL(COLOR,'') AS COLOR,
             ESTADO_VEHICULO,
             NVL(DESCRIPCION,'') AS DESCRIPCION
        FROM VEHICULOS
       WHERE ID_VEHICULO = p_id;
  END;

  PROCEDURE SP_INSERTAR(
    p_marca        IN VARCHAR2,
    p_modelo       IN VARCHAR2,
    p_ano          IN NUMBER,
    p_tipo         IN VARCHAR2,
    p_km           IN NUMBER,
    p_combustible  IN VARCHAR2,
    p_precio       IN NUMBER,
    p_color        IN VARCHAR2,
    p_descripcion  IN VARCHAR2,
    p_id_out       OUT NUMBER
  ) IS
  BEGIN
    INSERT INTO VEHICULOS(
      ID_VEHICULO, MARCA, MODELO, ANO_FABRICACION, TIPO_VEHICULO,
      KILOMETRAJE, TIPO_COMBUSTIBLE, PRECIO, ESTADO_VEHICULO, COLOR, DESCRIPCION
    ) VALUES (
      NULL,
      TRIM(p_marca),
      TRIM(p_modelo),
      p_ano,
      TRIM(p_tipo),
      NVL(p_km,0),
      TRIM(p_combustible),
      p_precio,
      'DISPONIBLE',
      TRIM(p_color),
      p_descripcion
    )
    RETURNING ID_VEHICULO INTO p_id_out;
  END;

  PROCEDURE SP_ACTUALIZAR(
    p_id           IN NUMBER,
    p_marca        IN VARCHAR2,
    p_modelo       IN VARCHAR2,
    p_ano          IN NUMBER,
    p_tipo         IN VARCHAR2,
    p_km           IN NUMBER,
    p_combustible  IN VARCHAR2,
    p_precio       IN NUMBER,
    p_color        IN VARCHAR2,
    p_descripcion  IN VARCHAR2
  ) IS
  BEGIN
    UPDATE VEHICULOS
       SET MARCA = TRIM(p_marca),
           MODELO = TRIM(p_modelo),
           ANO_FABRICACION = p_ano,
           TIPO_VEHICULO = TRIM(p_tipo),
           KILOMETRAJE = NVL(p_km,0),
           TIPO_COMBUSTIBLE = TRIM(p_combustible),
           PRECIO = p_precio,
           COLOR = TRIM(p_color),
           DESCRIPCION = p_descripcion
     WHERE ID_VEHICULO = p_id;

    IF SQL%ROWCOUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20021, 'Vehículo no existe');
    END IF;
  END;

  PROCEDURE SP_INACTIVAR(p_id IN NUMBER) IS
  BEGIN
    UPDATE VEHICULOS
       SET ESTADO_VEHICULO = 'INACTIVO'
     WHERE ID_VEHICULO = p_id;

    IF SQL%ROWCOUNT = 0 THEN
      RAISE_APPLICATION_ERROR(-20022, 'Vehículo no existe');
    END IF;
  END;

END PKG_VEHICULOS;
