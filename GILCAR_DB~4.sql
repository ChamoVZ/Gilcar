CREATE OR REPLACE PACKAGE BODY PKG_USUARIOS AS

  PROCEDURE SP_VALIDAR_LOGIN(
    p_correo      IN VARCHAR2,
    p_password    IN VARCHAR2,
    p_ok          OUT NUMBER,
    p_id_usuario  OUT NUMBER,
    p_id_rol      OUT NUMBER
  ) IS
  BEGIN
    p_ok := 0;
    p_id_usuario := NULL;
    p_id_rol := NULL;

    SELECT ID_USUARIO, ID_ROL
      INTO p_id_usuario, p_id_rol
      FROM USUARIOS
     WHERE CORREO = TRIM(LOWER(p_correo))
       AND CONTRASENA_ENCRIPTADA = p_password
       AND ESTADO_CUENTA = 'ACTIVO';

    p_ok := 1;

  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      p_ok := 0;
      p_id_usuario := NULL;
      p_id_rol := NULL;
  END SP_VALIDAR_LOGIN;

END PKG_USUARIOS;
